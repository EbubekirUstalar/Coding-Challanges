def can_exit(lst):

    if(lst[0][0] == 0):
        lst[0][0] = 2
    
    for i in range(len(lst)):
        for j in range(len(lst[0])):
            if(lst[i][j] == 0 and ((i+1 < len(lst) and lst[i+1][j] == 2) or (i > 0 and lst[i-1][j] == 2) or (j+1 < len(lst[0]) and lst[i][j+1] == 2) or (j > 0 and lst[i][j-1] == 2))):
                lst[i][j] = 2
                
    return True if (lst[-1][-1] == 2) else False


